<h1 style="text-align: center">Reviewing Standards for <? echo $Conf->shortName?> </h1>
<p style="text-align: left">Papers submitted to <?echo $Conf->shortName ?> are expected to be 
original, innovative works. Papers should be approximately 6000 words in length 
(which is about 20 pages with an 11pt font, 1.5 line spacing).</p>
<p style="text-align: left">The review process consists of four steps.</p>
<ol>
  <li>
  <p style="text-align: left">You should read the paper and enter your review 
  information. You can continue to return and edit your review information until 
  the end of the review period, as long as you have not &quot;finalized&quot; it.</li>
  <li>
  <p style="text-align: left">When you are done with your review &quot;finalize&quot; it 
  using the first menu selection, labeled &quot;Are you done with your review&quot;.</li>
  <li>
  <p style="text-align: left">When all reviews are finished, authors will be 
  able to read the section labeled &quot;Comments for the author&quot;. They will then be 
  able to respond to those comments. That is the <i>only</i> information that 
  authors will see.</li>
  <li>
  <p style="text-align: left">During program committee meeting, PC members will 
  use all the review information and the information provided by the author to 
  select papers for the conference.</li>
</ol>
<p style="text-align: left">When you review a paper, there are several things 
you need to tell us. This list follows the list of questions on the review form. 
When you change an entry on the form, you need to click the button labeled &quot;<b>Submit 
your paper review</b>&quot;. If you leave the page without clicking that button, your 
review entry will not be stored.</p>
<ul>
  <li>
  <p style="text-align: left">&nbsp;<b>Are you finished with this review?<br>
  </b>Once you &quot;finalize&quot; this review, you can not modify it any further. Be 
  careful with this option.</li>
  <li>
  <p style="text-align: left">&nbsp;<b>Provide a short summary of the paper<br>
  </b>You should briefly summarize the paper, including the strongest 
  contributions that the paper makes.</li>
  <li>
  <p style="text-align: left">&nbsp;<b>Your qualifications to review this paper<br>
  </b>We realize that not all reviewers are equally qualified; accurately 
  assessing your background will help the program committee understand your 
  review.</li>
  <li>
  <p style="text-align: left"><b>&nbsp;Overall paper merit<br>
  </b>This is the single most important decision concerning the paper - do you 
  think it should be accepted to the conference? If you think the paper should 
  be a short paper, you <b><i>must</i></b> indicate that it would be an 
  acceptable short paper and you should leave a comment to the program committee 
  to that effect.</li>
  <li>
  <p style="text-align: left">&nbsp;<b>Novelty of paper<br>
  </b>This conference focuses on novel work; it's acceptable that a novel idea 
  have slightly less stringent evaluation than an incremental improvement, so we 
  need to know how novel the work is. If you think the paper is <b><i>not</i></b> 
  novel, you should clearly state why and indicate related prior work in your 
  comments to the author or program committee.</li>
  <li>
  <p style="text-align: left">&nbsp;<b>Was the paper technically sound?<br>
  </b>Was the experimental method reasonable? Good benchmarks? Good assumptions?</li>
  <li>
  <p style="text-align: left">&nbsp;<b>Interest to community<br>
  </b>Do you think the paper would engender a lot of discussion? Do you think 
  the average attendee would attend the paper presentation?</li>
  <li>
  <p style="text-align: left">&nbsp;<b>Will the paper be important over time?<br>
  </b>Is this a lasting paper, one that will be cited for years?</li>
  <li>
  <p style="text-align: left">&nbsp;<b>Spelling and grammar?<br>
  </b>Is the paper readable? If there are &quot;show stopper&quot; issues, you should 
  inform the program committee.</li>
  <li>
  <p style="text-align: left">&nbsp;<b>Would this be a good short paper?<br>
  </b>Occasionally, papers present a small but elegant idea, or an incompletely 
  evaluated novel idea. These papers may be more appropriate for a short (5-7 
  pages rather than 10 pages) paper. Would this paper be appropriate for that?</li>
  <li>
  <p style="text-align: left">&nbsp;<b>Detailed comments to the author<br>
  </b>This is the single most important response you can provide to the author. 
  One of the critical roles of a review process is to help authors understand 
  any problems with their work so they can improve it. Please spend the time to 
  indicate to the author and program committee <b><i>&nbsp;why</i></b> you've 
  rated the paper the way you did.</li>
  <li>
  <p style="text-align: left">&nbsp;<b>Additional comments to the program 
  committee<br>
  </b>There may be reasons that influence your rating of the paper that would 
  reveal your identity, or that you would only like made available to the 
  program committee. Please elaborate if there are any such issues; these 
  comments will be held in confidence.</li>
</ul>
